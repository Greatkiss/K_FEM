def BC(u, v, p, Nw, Nh):
    #left
    u[0,:] = 1.0
    v[0,:] = v[1,:]
    p[0,:] = 2.0
    #down
    u[:,0] = u[:,1]
    v[:,0] = v[:,1]
    p[:,0] = 1.0
    #right
    u[Nw+1,:] = 0.0
    v[Nw+1,:] = 0.0
    p[Nw+1,:] = p[Nw,:]
    #up
    u[:,Nh+1] = 0.0
    v[:,Nh+1] = 0.0
    p[:,Nh+1] = p[:,Nh]
    #again
    u[0,:] = 1.0
    p[0,:] = 2.0
    p[:,0] = 1.0